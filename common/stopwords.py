def get_stopwords(language='eng'):
    if language is None:
        print("No language specified, returning empty set for stopwords")
        return set()
    if language == 'eng':
        return stopwords_english
    elif language == 'ger':
        return stopwords_german
    elif language == 'lat':
        return stopwords_latin
    elif language == 'swe':
        return stopwords_swedish
    else:
        ValueError("Language not supported", language)